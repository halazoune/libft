#include <stddef.h>
#include "libft/containers/list.h"

t_lst			*ft_lstpushback(t_lst **alst, void *data)
{
	t_lst		*node;

	node = ft_lstnew(data);
	if (node == NULL)
		return (NULL);
	ft_lstaddback(alst, node);
	return (node);
}
