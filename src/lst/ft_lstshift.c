#include "libft.h"

void	ft_lstshift(t_lst **alst)
{
	t_lst	*new_first;

	if (alst != NULL && *alst != NULL)
	{
		new_first = (*alst)->next;
		free(*alst);
		*alst = new_first;
	}
}
