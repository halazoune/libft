#include "libft.h"

int				ft_chrpos(const char *chrs, char c)
{
	int			i;

	if (c == '\0')
		return (-1);
	i = 0;
	while (chrs[i] != '\0')
	{
		if (chrs[i] == c)
			return (i);
		i++;
	}
	return (-1);
}
