#include "libft.h"

int				ft_strccmp(const char *s1, const char *s2, char c)
{
	size_t		i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && s2[i] != c)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	if (s1[i] == s2[i] || s2[i] == c)
		return (0);
	return (s1[i] - s2[i]);
}
