#include "libft/basics/chars.h"

void			ft_putptr(size_t nbr)
{
	if (nbr < 10)
		ft_putchr('0' + nbr);
	else
	{
		ft_putptr(nbr / 10);
		ft_putchr('0' + (nbr % 10));
	}
}
