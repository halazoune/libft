unsigned int	ft_nbrlen(int nbr)
{
	unsigned int	len;

	len = (nbr < 0) ? 2 : 1;
	while (nbr < -9 || nbr > 9)
	{
		nbr /= 10;
		len++;
	}
	return (len);
}
